function [topo,X,Y] = getSyntheticTopo()
A0=1;
A1=0.01;
A2=0.03;
B0=1;
B1=2;
lamda_x1=5;
lamda_x2=40;
lamda_x3=25;
lamda_x4=8;
C0=3;
C1=15;
D0=6;
D1=8;
E0=2;
E1=5;
lamda_y1=15; % y-frequency is 0.1;
lamda_y2=10; % y-frequency is 0.05;
lamda_y3=40; % y-frequency is 0.025
lamda_y4=5;  % y-frequency is 0.2;
sigma=0.00001;
[X_up,Y_up] = meshgrid(1:1000,1:250);
% topo = A0+A1*X+A2*Y+B0*sin(2*pi*X/lamda_x1)+B1*sin(2*pi*X/lamda_x2)+C0*sin(2*pi*Y/lamda_y1)+C1*sin(2*pi*Y/lamda_y2)+B0*sin(2*pi*X/lamda_x1).*sin(2*pi*Y/lamda_y1)+B1*sin(2*pi*X/lamda_x2).*sin(2*pi*Y/lamda_y2);
% topo_up = A0+A1*X_up+A2*Y_up+B0*sin(2*pi*X_up/lamda_x3)+B1*sin(2*pi*X_up/lamda_x4)+C0*sin(2*pi*Y_up/lamda_y3)+C1*sin(2*pi*Y_up/lamda_y4)+B0*sin(2*pi*X_up/lamda_x1).*sin(2*pi*Y_up/lamda_y1)+B1*sin(2*pi*X_up/lamda_x2).*sin(2*pi*Y_up/lamda_y2);
topo_up=A0+A1*X_up+A2*Y_up+B0*sin(2*pi*X_up/lamda_x2).*sin(2*pi*Y_up/lamda_y2)+B1*sin(2*pi*X_up/lamda_x3);
[X_down,Y_down] = meshgrid(1:1000,251:500);
topo_dn=A0+A1*X_down+A2*Y_down+B0*sin(2*pi*X_down/lamda_x1).*sin(2*pi*Y_down/lamda_y1)+B1*sin(2*pi*X_down/lamda_x4);
topo=[topo_up;topo_dn];
% topo = A0+A1*X+A2*Y+B0*sin(2*pi*X/lamda_x1).*sin(2*pi*Y/lamda_y1);
end